package com.example.webhooksserver.mapper;

import com.example.webhooksserver.domain.PushDetail;
import com.example.webhooksserver.dtos.PushDetailDto;

import org.modelmapper.ModelMapper;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.expression.ParseException;

public class PushDetailDtoToEntity {

    // private ModelMapper modelMapper = new ModelMapper();

    public PushDetail convertToEntity(PushDetailDto prDetailDto) throws ParseException {
        // PrDetail prDetail = modelMapper.map(prDetailDto, PrDetail.class);
        PushDetail prDetail = new PushDetail();
        prDetail.setSender(prDetailDto.getSender().toString());
        prDetail.setRepository(prDetailDto.getRepository().toString());
        prDetail.setCompare(prDetailDto.getCompare() + ".diff");
        return prDetail;
    }
}